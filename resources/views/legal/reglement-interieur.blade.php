        <h1 class="font-semibold text-2xl text-black mb-4">Règlement Intérieur
            de l’Organisme de Formation Solaru Form
        </h1>
        <p class="text-black/50 mt-4">
            Etabli Conformément Aux Articles L6352-3 et L6352-4 et R6352-1 à R6352 15 Du Code Du travail
        </p>

        <h2 class="font-semibold text-xl text-black uppercase mt-8">
            PREAMBULE
        </h2>
        <h3 class="mt-2 underline">Article 1 – Objet et Champ D’Application Du Règlement</h3>
        <p class="text-black/50 mt-4">
            Le présent règlement s’applique à toutes les personnes participantes à une action de formation organisée par
            l’Organisme de Formation Solaru Form. Un exemplaire est remis à chaque stagiaire. Le règlement définit les
            règles d’hygiène et de sécurité, les règles générales et permanentes relatives à la discipline ainsi que la
            nature et l’échelle des sanctions pouvant être prises vis-à-vis des stagiaires qui y contreviennent et les
            garanties procédurales applicables lorsqu’une sanction est envisagée. Toute personne doit respecter les
            termes du présent règlement durant toute la durée de l’action de formation.
        </p>

        <h2 class="font-semibold text-xl text-black uppercase mt-8">
            SECTION 1 : REGLES D’HYGIENE ET DE SÉCURITÉ
        </h2>
        <h3 class="mt-2 underline">Article 2 - Principes généraux</h3>
        <p class="text-black/50 mt-4">
            La prévention des risques d’accidents et de maladies est impérative et exige de chacun le respect :<br />
            Des prescriptions applicables en matière d’hygiène et de sécurité sur les lieux de formation ;<br />
            De toute consigne imposée soit par la direction de l’organisme de formation soit par le constructeur ou le
            formateur s’agissant notamment de l’usage des matériels mis à disposition.<br />
            Chaque stagiaire doit ainsi veiller à sa sécurité personnelle et à celle des autres en respectant, en
            fonction de sa formation, les consignes générales et particulières en matière d’hygiène et de sécurité. S’il
            constate un dysfonctionnement du système de sécurité, il en avertit immédiatement la direction de
            l’organisme de formation. Le non-respect de ces consignes expose la personne à des sanctions disciplinaires.
        </p>
        <h3 class="mt-2 underline">Article 3 - Consignes d’incendie</h3>
        <p class="text-black/50 mt-4">
            Les consignes d’incendie et notamment un plan de localisation des extincteurs et des issues de secours sont
            affichés dans les locaux de l’organisme de formation. Le stagiaire doit en prendre connaissance. En cas
            d’alerte, le stagiaire doit cesser toute activité de formation et suivre dans le calme les instructions du
            représentant habilité de l’organisme de formation ou des services de secours. Tout stagiaire témoin d’un
            début d’incendie doit immédiatement appeler les secours en composant le 18 à partir d’un téléphone fixe ou
            le 112 à partir d’un téléphone portable et alerter un représentant de l’organisme de formation.

        </p>
        <h3 class="mt-2 underline">Article 4 - Boissons Alcoolisées et Drogues</h3>
        <p class="text-black/50 mt-4">
            L’introduction ou la consommation de drogue ou de boissons alcoolisées dans les locaux est formellement
            interdite. Il est interdit aux stagiaires de pénétrer ou de séjourner en état d’ivresse ou sous l’emprise de
            drogue dans l’organisme de formation. Les stagiaires auront accès lors des pauses aux postes de distribution
            de boissons non alcoolisées.

        </p>
        <h3 class="mt-2 underline">Article 5 - Interdiction De Fumer</h3>
        <p class="text-black/50 mt-4">
            Il est formellement interdit de fumer dans les salles de formation et plus généralement dans l’enceinte de
            l’organisme de formation.

        </p>
        <h3 class="mt-2 underline">Article 6 - Accident</h3>
        <p class="text-black/50 mt-4">
            Le stagiaire victime d’un accident - survenu pendant la formation ou pendant le temps de trajet entre le
            lieu de formation et son domicile ou son lieu de travail – ou le témoin de cet accident avertit
            immédiatement la direction de l’organisme de formation. Le responsable de l’organisme de formation
            entreprend les démarches appropriées en matière de soins et réalise la déclaration auprès de la caisse de
            Sécurité sociale compétente.

        </p>

        <h2 class="font-semibold text-xl text-black uppercase mt-8">
            SECTION 2 : DISCIPLINE GENERALE
        </h2>
        <h3 class="mt-2 underline">Article 7 - Assiduité Du Stagiaire en Formation</h3>
        <p class="text-black/50 mt-4 italic">
            Article 7.1. - horaires de formation
        </p>
        <p class="text-black/50 mt-2">
            Les stagiaires doivent se conformer aux horaires fixés et communiqués au préalable par l’organisme de
            formation. Le non-respect de ces horaires peut entraîner des sanctions. Sauf circonstances exceptionnelles,
            les stagiaires ne peuvent s’absenter pendant les heures de stage.
        </p>
        <p class="text-black/50 mt-4 italic">
            Article 7.2. - Absences, retards ou départs anticipés
        </p>
        <p class="text-black/50 mt-2">
            En cas d’absence, de retard ou de départ avant l’horaire prévu, les stagiaires doivent avertir l’organisme
            de formation et s’en justifier. L’organisme de formation informe immédiatement le financeur (employeur,
            administration, Fongecif, Région, Pôle emploi,) de cet événement. Tout événement non justifié par des
            circonstances particulières constitue une faute passible de sanctions disciplinaires. De plus, conformément
            à l’article R6341-45 du Code du travail, le stagiaire – dont la rémunération est prise en charge par les
            pouvoirs publics – s’expose à une retenue sur sa rémunération de stage proportionnelle à la durée de
            l’absence.
        </p>
        <p class="text-black/50 mt-4 italic">
            Article 7.3. - Formalisme attaché au suivi de la formation
        </p>
        <p class="text-black/50 mt-2">
            Le stagiaire est tenu de renseigner la feuille d’émargement au fur et à mesure du déroulement de l’action.
            Il peut lui être demandé de réaliser un bilan de la formation. A l’issue de l’action de formation, il se
            voit remettre une attestation de fin de formation et une attestation de présence au stage à transmettre,
            selon le cas, à son employeur/administration ou à l’organisme qui finance l’action. Le stagiaire remet, dans
            les meilleurs délais, à l’organisme de formation les documents qu’il doit renseigner en tant que prestataire
            (demande de rémunération ou de prise en charges des frais liés à la formation ; attestations d’inscription
            ou d’entrée en stage…).
        </p>

        <h3 class="mt-2 underline">Article 8 - Accès Aux Locaux De Formation</h3>
        <p class="text-black/50 mt-4">
            Sauf autorisation expresse de la direction de l’organisme de formation, le stagiaire ne peut :<br />
            - Entrer ou demeurer dans les locaux de formation à d’autres fins que la formation ;<br />
            - Y introduire, faire introduire ou faciliter l’introduction de personnes étrangères à l’organisme ;<br />
            - Procéder, dans ces derniers, à la vente de biens ou de services.
        </p>

        <h3 class="mt-2 underline">Article 9 – Tenue</h3>
        <p class="text-black/50 mt-4">
            Le stagiaire est invité à se présenter à l’organisme en tenue vestimentaire correcte. Des prescriptions
            vestimentaires spécifiques peuvent être édictées et transmises au stagiaire pour des formations exposant ce
            dernier à des risques particuliers en raison de l’espace de formation ou des matériaux utilisés.
        </p>

        <h3 class="mt-2 underline">Article 10 – Comportement</h3>
        <p class="text-black/50 mt-4">
            Il est demandé à tout stagiaire d’avoir un comportement garantissant le respect des règles élémentaires de
            savoir vivre, de savoir être en collectivité et le bon déroulement des formations.
        </p>

        <h3 class="mt-2 underline">Article 11 - Utilisation du matériel</h3>
        <p class="text-black/50 mt-4">
            Sauf autorisation particulière de la direction de l’organisme de formation, l’usage du matériel de formation
            se fait sur les lieux de formation et est exclusivement réservé à l’activité de formation. L’utilisation du
            matériel à des fins personnelles est interdite. Le stagiaire est tenu de conserver en bon état le matériel
            qui lui est confié pour la formation.
            <br /><br />
            Il doit en faire un usage conforme à son objet et selon les règles délivrées par le formateur. Le stagiaire
            signale immédiatement au formateur toute anomalie du matériel.
        </p>

        <h2 class="font-semibold text-xl text-black uppercase mt-8">
            SECTION 3 : MESURES DISCIPLINAIRES
        </h2>
        <h3 class="mt-2 underline">Article 12 - Sanctions Disciplinaires</h3>
        <p class="text-black/50 mt-4">
            Tout manquement du stagiaire à l’une des prescriptions du présent règlement intérieur pourra faire l’objet
            d’une sanction prononcée par le responsable de l’organisme de formation ou son représentant. Tout agissement
            considéré comme fautif pourra, en fonction de sa nature et de sa gravité, faire l’objet de l’une ou l’autre
            des sanctions suivantes :<br />
            - Rappel à l’ordre ;<br />
            - Avertissement écrit par le directeur de l’organisme de formation ou par son représentant ;<br />
            - Exclusion temporaire de la formation ; Exclusion définitive de la formation.<br />
            <br /><br />
            Les amendes ou autres sanctions pécuniaires sont interdites. Le responsable de l’organisme de formation ou
            son représentant informe de la sanction prise : L’employeur du salarié stagiaire ou l’administration de
            l’agent stagiaire (NDLR : uniquement quand la formation se réalise sur commande de l’employeur ou de
            l’administration) Et/ou le financeur du stage.
        </p>
        <h3 class="mt-2 underline">Article 13 - Garanties Disciplinaires</h3>
        <p class="text-black/50 mt-4 italic">
            Article 13.1. – Information du stagiaire
        </p>
        <p class="text-black/50 mt-2">
            Aucune sanction ne peut être infligée au stagiaire sans que celui-ci ait été informé au préalable des griefs
            retenus contre lui. Toutefois, lorsqu’un agissement, considéré comme fautif, a rendu indispensable une
            mesure conservatoire d’exclusion temporaire à effet immédiat, aucune sanction définitive relative à cet
            agissement ne peut être prise sans que le stagiaire n’ait été au préalable informé des griefs retenus contre
            lui et éventuellement, que la procédure ci-après décrite ait été respectée.
        </p>
        <p class="text-black/50 mt-4 italic">
            Article 13.2. – Convocation pour un entretien
        </p>
        <p class="text-black/50 mt-2">
            Lorsque le gérant de l’organisme de formation ou son représentant envisage de prendre une sanction, il est
            procédé de la manière suivante. Il convoque le stagiaire – par lettre recommandée avec demande d’accusé de
            réception ou remise à l’intéressé contre décharge – en lui indiquant l’objet de la convocation.
            La convocation indique également la date, l’heure et le lieu de l’entretien ainsi que la possibilité de se
            faire assister par une personne de son choix stagiaire ou salarié de l’organisme de formation.
        </p>
        <p class="text-black/50 mt-4 italic">
            Article 13.3. – Assistance possible pendant l’entretien
        </p>
        <p class="text-black/50 mt-2">
            Au cours de l’entretien, le stagiaire peut se faire assister par une personne de son choix, notamment le
            délégué du stage. Le gérant ou son représentant indique le motif de la sanction envisagée et recueille les
            explications du stagiaire.
        </p>
        <p class="text-black/50 mt-4 italic">
            Article 13.4. – Prononcé de la sanction
        </p>
        <p class="text-black/50 mt-2">
            La sanction ne peut intervenir moins d’un jour franc ni plus de quinze jours après l’entretien. La sanction
            fait l’objet d’une notification écrite et motivée au stagiaire sous forme d’une lettre recommandée ou remise
            contre décharge.
        </p>

        <h2 class="font-semibold text-xl text-black uppercase mt-8">
            SECTION 4 : REPRÉSENTATION DES STAGIAIRES
        </h2>
        <h3 class="mt-2 underline">Article 14 – Organisation des élections</h3>
        <p class="text-black/50 mt-4">
            Dans les stages d’une durée supérieure à 500 heures, il est procédé simultanément à l’élection d’un délégué
            titulaire et d’un délégué suppléant au scrutin uninominal à deux tours, selon les modalités suivantes : Tous
            les stagiaires sont électeurs et éligibles sauf les détenus. Le scrutin a lieu, pendant les heures de la
            formation, au plus tôt 20 heures et au plus tard 40 heures après le début du stage ; le responsable de
            l’organisme de formation à la charge de l’organisation du scrutin. Il en assure le bon déroulement. Il
            adresse un procès-verbal de carence, transmis au préfet de région territorialement compétent, lorsque la
            représentation des stagiaires ne peut être assurée.
        </p>
        <h3 class="mt-2 underline">Article 15 – Durée du mandat des délégués des stagiaires</h3>
        <p class="text-black/50 mt-4">
            Les délégués sont élus pour la durée du stage. Leurs fonctions prennent fin lorsqu’ils cessent, pour quelque
            cause que ce soit, de participer au stage. Si le délégué titulaire et le délégué suppléant ont cessé leurs
            fonctions avant la fin du stage, il est procédé à une nouvelle élection.
        </p>
        <h3 class="mt-2 underline">Article 16 – Rôle des délégués des stagiaires</h3>
        <p class="text-black/50 mt-4">
            Les délégués font toute suggestion pour améliorer le déroulement des stages et les conditions de vie des
            stagiaires dans l’organisme de formation. Ils présentent toutes les réclamations individuelles ou
            collectives relatives à ces matières, aux conditions d’hygiène et de sécurité et à l’application du
            règlement intérieur.
        </p>

